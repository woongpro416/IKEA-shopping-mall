COMMENT ON TABLE category IS '상품 카테고리';
COMMENT ON TABLE members IS '회원';
COMMENT ON TABLE products IS '상품';
COMMENT ON TABLE product_stocks IS '상품 재고';
COMMENT ON TABLE carts IS '장바구니(회원 또는 비회원)';
COMMENT ON TABLE cart_items IS '장바구니 상품';
COMMENT ON TABLE notices IS '공지사항';
